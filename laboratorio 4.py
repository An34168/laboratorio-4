# Определяем доступные предметы
предметы = [
    {'название': 'Ингалятор', 'сокращение': 'i', 'размер': 1, 'очки': 5},
    {'название': 'Винтовка', 'сокращение': 'r', 'размер': 3, 'очки': 25},
    {'название': 'Пистолет', 'сокращение': 'p', 'размер': 2, 'очки': 15},
    {'название': 'Патроны', 'сокращение': 'a', 'размер': 2, 'очки': 15},
    {'название': 'Аптечка', 'сокращение': 'm', 'размер': 2, 'очки': 20},
    {'название': 'Нож', 'сокращение': 'k', 'размер': 1, 'очки': 15},
    {'название': 'Топор', 'сокращение': 'x', 'размер': 3, 'очки': 20},
    {'название': 'Амулет', 'сокращение': 't', 'размер': 1, 'очки': 25},
    {'название': 'Фляжка', 'сокращение': 'f', 'размер': 1, 'очки': 15},
    {'название': 'Антидот', 'сокращение': 'd', 'размер': 1, 'очки': 10},
    {'название': 'Еда', 'сокращение': 's', 'размер': 2, 'очки': 20},
    {'название': 'Арбалет', 'сокращение': 'c', 'размер': 2, 'очки': 20}
]

# Общий размер рюкзака (3x3)
вместимость = 9

# Поскольку у Тома астма, ингалятор обязательно должен быть в рюкзаке
ингалятор = [obj for obj in предметы if obj['сокращение'] == 'i'][0]

# Удаляем ингалятор из списка, потому что он уже выбран
предметы = [obj for obj in предметы if obj['сокращение'] != 'i']

# Сортируем оставшиеся предметы по очкам выживания (по убыванию)
предметы.sort(key=lambda x: x['очки'], reverse=True)

# Инициализируем рюкзак с ингалятором
рюкзак = [ингалятор]
место_доступно = вместимость - ингалятор['размер']
очки_всего = ингалятор['очки']

# Выбираем оставшиеся предметы
for obj in предметы:
    if место_доступно >= obj['размер']:
        рюкзак.append(obj)
        место_доступно -= obj['размер']
        очки_всего += obj['очки']

# Печатаем результат
# Отображаем итоговый инвентарь в формате 3x3
инвентарь = [[''] * 3 for _ in range(3)]

# Заполняем инвентарь выбранными предметами
индекс = 0
for i in range(3):
    for j in range(3):
        if индекс < len(рюкзак):
            инвентарь[i][j] = рюкзак[индекс]['сокращение']
            индекс += 1

# Печатаем инвентарь в виде матрицы
for ряд in инвентарь:
    print(ряд)

print(f"\nОбщие очки выживания: {очки_всего}")


